let tokenContract;
let userAccount;

async function init() {
    const accounts = await web3.eth.getAccounts();
    userAccount = accounts[0];
    
    tokenContract = new web3.eth.Contract(contractABI, contractAddress);

    updateBalances();
}

// Update ETH and Token balances
async function updateBalances() {
    const ethBalance = await web3.eth.getBalance(userAccount);
    document.getElementById('ethBalance').innerText = web3.utils.fromWei(ethBalance, 'ether');

    const tokenBalance = await tokenContract.methods.balanceOf(userAccount).call();
    document.getElementById('tokenBalance').innerText = tokenBalance;
}

// Buy NGT Tokens with ETH
document.getElementById('buyToken').onclick = async () => {
    const ethAmount = document.getElementById('ethAmount').value;
    const ethValueInWei = web3.utils.toWei(ethAmount, 'ether');

    await web3.eth.sendTransaction({
        from: userAccount,
        to: contractAddress,
        value: ethValueInWei
    });

    updateBalances();
};

// Transfer NGT Tokens
document.getElementById('transferToken').onclick = async () => {
    const recipient = document.getElementById('transferTo').value;
    const amount = document.getElementById('transferAmount').value;

    await tokenContract.methods.transfer(recipient, amount).send({ from: userAccount });
    updateBalances();
};

// Approve NGT Tokens for a Spender
document.getElementById('approveToken').onclick = async () => {
    const spender = document.getElementById('approveSpender').value;
    const amount = document.getElementById('approveAmount').value;

    await tokenContract.methods.approve(spender, amount).send({ from: userAccount });
    updateBalances();
};
